    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            int a, b, c, d;
            a = int.Parse(txtIncome.Text);
            b = int.Parse(txtExpenditure.Text);
            c = int.Parse(txtWant.Text);

            d = c / (a - b);
            txtAnswer.Text = Convert.ToString(d);
        }
    }
